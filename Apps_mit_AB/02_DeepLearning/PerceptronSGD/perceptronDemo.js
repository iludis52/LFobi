function makeStochasticData() {
  tempData = [];
  StochInd = [];
  for (i=0; i<Data.length; i++) {
    tempData.push(i);
  }
  for (j=0; j<Data.length; j++) {
    tempIndex = Math.floor(Math.random() * tempData.length);
    StochInd.push(tempData[tempIndex]);
    tempData.splice(tempIndex, 1);
  }
  aktIndex =0;
  stateMachine = 1;
}
//---------------------------------------------------------------
function makeStochasticGradientDescent() {
  error = 0;
  for (i=0; i<Data.length; i++) {
    makePerceptron(i);
  }
  makeGGleichungII();
  stateMachine = 2;
}
//---------------------------------------------------------------
function makePerceptron(aktIndex) {
  tempX = Data[aktIndex][0];
  tempY = Data[aktIndex][1];
  tempZ = Data[aktIndex][2];
  side = lineC[0]*tempX+lineC[1]*tempY-lineC[2];
  region = Math.sign((side));
  if (region<0 && tempZ=="b") {
    error = error + Math.abs(side);
    lineC[0] = lineC[0]+tempX*learnrate*Math.abs(side);
    lineC[1] = lineC[1]+tempY*learnrate*Math.abs(side);
    lineC[2] = lineC[2]+learnrate*Math.abs(side);
  }
  if (region>0 && tempZ=="a") {
    error = error + Math.abs(side);
    lineC[0] = lineC[0]-tempX*learnrate*Math.abs(side);
    lineC[1] = lineC[1]-tempY*learnrate*Math.abs(side);
    lineC[2] = lineC[2]-learnrate*Math.abs(side);
  }
}

//---------------------------------------------------------------
function makeGGleichungII() {
  lFunc[0] = -lineC[0]/lineC[1];
  lFunc[1] = lineC[2]/lineC[1];
  dirL = Math.sqrt(lineC[0]*lineC[0]+lineC[1]*lineC[1]);
  //dirL = 1;
  dirV[1] =-lineC[0]/dirL;
  dirV[0] = lineC[1]/dirL;
  midV = [10, lFunc[1]+lFunc[0]*10];
  makeTextarea();
}
function makeTextarea() {
  tArea.elt.value = "learning rate: " + learnrate +"\n";
  tArea.elt.value += "epoch: " + epochCurr + "  Error: "+ Math.round(error*100)/100 + "\n";
  tArea.elt.value += "line coordinates: " + Math.round(lineC[0]*100)/100 + "*x + " + Math.round(lineC[1]*100)/100 + "*y = " + Math.round(lineC[2]*100)/100 + "\n";
  tArea.elt.value += "linear function: y = " + Math.round(100*lFunc[0])/100 + "*x + " + Math.round(100*lFunc[1])/100;
}
