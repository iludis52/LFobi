function Initialize() {
  stateMachine = 0;
  epochMax = 1000;
  epochCurr = 0;
  lineC[0] = 1-2*Math.random();
  lineC[1] = 1-2*Math.random();
  lineC[2] = 10*lineC[0] + 10*lineC[1];
  makeGGleichungII();
}
