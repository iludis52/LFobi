function calcAllDistToLine() {
  Distance = [];
  mult = [];
  for (i = 0; i < Data.length; i++) {
    mult.push((Data[i][0] - midV[0]) * dirV[0] + (Data[i][1] - midV[1]) * dirV[1]);
    distX = midV[0] + mult[i] * dirV[0] - Data[i][0];
    distY = midV[1] + mult[i] * dirV[1] - Data[i][1];
    seitePunkt = Math.sign(Data[i][0] * lineC[0] + Data[i][1] * lineC[1] - lineC[2]);
    Distance.push(seitePunkt*Math.sqrt(distX * distX + distY * distY));
  }
}
