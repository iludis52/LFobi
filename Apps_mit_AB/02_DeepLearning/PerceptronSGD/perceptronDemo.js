function makeMarginLine() {
  c = [];
  for (i=0; i<20; i=i+2/Width) {
    py = lFunc[1] + lFunc[0]*i;
    if (py>0 && py<20) {
      c.push([i, py]);
    }
  }
  if (c.length>0) {
    line(sX(c[0][0]), sY(c[0][1]), sX(c[c.length-1][0]), sY(c[c.length-1][1]));
  }
}
