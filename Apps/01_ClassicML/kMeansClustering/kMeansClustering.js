//---------------------------------------------------------------
function Initialize() {
  numCentroids = kSlider.value();
  CCoord = [];
  for (i = 0; i < numCentroids; i++) {
    CCoord.push([Math.random() * 20, Math.random() * 20]);
    CCoordAlt.push([Math.random() * 20, Math.random() * 20]);
  }
  stateMachine = 1;
}

//---------------------------------------------------------------
function calcDistToCentroids() {
  Distances = [];
  labels = [];
  for (i = 0; i < Data.length; i++) {
    Distances.push([]);
    for (j = 0; j < numCentroids; j++) {
      Distances[i].push(dist(Data[i][0], Data[i][1], CCoord[j][0], CCoord[j][1]));
    }
    minCentIndex = 0;
    minCent = Distances[i][minCentIndex];
    for (k = 0; k < numCentroids; k++) {
      if (minCent > Distances[i][k]) {
        minCent = Distances[i][k];
        minCentIndex = k;
      }
    }
    labels.push(minCentIndex);
  }
  drawAllLines();
  stateMachine = 2;
}
//---------------------------------------------------------------
function drawAllLines() {
  strokeWeight(1);
  for (i = 0; i < Data.length; i++) {
    j = labels[i];
    makeColor(j);
    line(sX(Data[i][0]), sY(Data[i][1]), sX(CCoord[j][0]), sY(CCoord[j][1]));
  }
  for (k = 0; k < numCentroids; k++) {
    makeColor(k);
    rect(sX(CCoord[k][0]) - 8, sY(CCoord[k][1]) - 8, 16, 16);
  }
}
//---------------------------------------------------------------
function moveCentroidsToCenter() {
  Centmean = [];
  for (k = 0; k < numCentroids; k++) {
    Centmean.push([0, 0, 0]);
  }
  for (i = 0; i < Data.length; i++) {
    j = labels[i];
    Centmean[j][0] = Centmean[j][0] + Data[i][0];
    Centmean[j][1] = Centmean[j][1] + Data[i][1];
    Centmean[j][2] = Centmean[j][2] + 1;
  }
  for (k = 0; k < numCentroids; k++) {
    CCoord[k][0] = Centmean[k][0] / Centmean[k][2];
    CCoord[k][1] = Centmean[k][1] / Centmean[k][2];
  }
  withinClusterSumOfSquare();
  drawAllLines();
  stateMachine = 3;
}
//---------------------------------------------------------------
function testIfConverged() {
  fertig = true;
  for (k = 0; k < numCentroids; k++) {
    if ((CCoord[k][0]!=CCoordAlt[k][0])||(CCoord[k][1]!=CCoordAlt[k][1])) {
      fertig = false;
    }
  }
  if (fertig) {

    stateMachine = 4;
  } else {
    stateMachine = 1;
  }
  for (m=0; m<CCoord.length; m++) {
    CCoordAlt[m][0] = CCoord[m][0];
    CCoordAlt[m][1] = CCoord[m][1];
  }
}
//---------------------------------------------------------------
function withinClusterSumOfSquare() {
  WSS = 0;
  for (i = 0; i < Data.length; i++) {
    WSS = WSS + Math.pow(dist(Data[i][0], Data[i][1], CCoord[labels[i]][0], CCoord[labels[i]][1]), 2);
  }
  if (WSSArray[numCentroids]!=0) {
    if (WSSArray[numCentroids]>WSS) {
      WSSArray[numCentroids]=WSS;
    }
  } else {
    WSSArray[numCentroids]=WSS;
  }
}
