function makeTextarea() {
  tArea.elt.value = "X value: " + xVal.toString() +" | Y value: " + yVal.toString() + "\n"+ "\n";
  tArea.elt.value += "Distance to Point Nr."+ "\n";
  for (i=0; i<lowestValues.length; i++) {
    j= lowestValues[i][0];
    d= lowestValues[i][1];
    tArea.elt.value += "("+ (i+1).toString() + ") [" + Data[j][0].toString()+ "," + Data[j][1].toString()+ "]:  " + d + "  (class "+ Data[j][2]+")" +"\n";
  }
  tArea.elt.value +="\n"+ "Count a (red Dots): " + countA + " | Count b (blue Dots): " + countB + "\n";
  tArea.elt.value +="Classification: Member of Class " +currClass;
}
