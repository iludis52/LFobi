function draw() {
  inDiagram = (mouseX>margin) && (mouseX<Width + margin) && (mouseY>margin) && (mouseY<Width + margin);
  background(220);
  k = kSlider.value();
  makeCoord(Width, margin, padding, scale);
  drawData();

  if (inDiagram) {
    xVal = calcDataX(mouseX);
    yVal = calcDataY(mouseY);
    pruefekNN();
  } else {
    xVal = yVal = 0;
  }
}
function calcDataX(ScreenX) {
  return (ScreenX-margin)/scale;
}
function calcDataY(ScreenY) {
  return (Width - ScreenY+margin)/scale;
}
function calcScreenX(DataX) {
  return margin + scale*DataX;
}
function calcScreenY(DataY) {
  return margin + Width - scale*DataY;
}
function drawData() {
  for (i=0; i<Data.length; i++) {
    if (Data[i][2]=="a") {
      fill(255, 0, 0);
    }
    if (Data[i][2]=="b") {
      fill(0, 0, 255);
    }
    circle(calcScreenX(Data[i][0]), calcScreenY(Data[i][1]), 10);
  }
  for (j=0; j<newPt.length; j++) {
    if (newPt[j][2]=="a") {
      fill(255, 0, 0);
    }
    if (newPt[j][2]=="b") {
      fill(0, 0, 255);
    }
    rect(calcScreenX(newPt[j][0])-4, calcScreenY(newPt[j][1])-4, 8, 8);
  }
}
function pruefekNN() {
  distance=[];
  lowestValues=[];
  countA=0;
  countB=0;
  let switchOff;
  for (i=0; i<Data.length; i++) {
    let distanceX = xVal - Data[i][0];
    let distanceY = yVal - Data[i][1];
    let euclideanD = Math.sqrt(distanceX*distanceX + distanceY*distanceY);
    distance.push([euclideanD, i, Data[i][2]]);
  }
  for (j=0; j<k; j++) {
    let min = distance[0][0];
    let minIndex = distance[0][1];
    switchOff = 0;
    for (i=1; i<distance.length; i++) {
      if (distance[i][0]<min) {
        min = distance[i][0];
        minIndex = distance[i][1];
        switchOff = i;
      }
    }
    if (distance[switchOff][2]=="a") {
      countA++;
    }
    if (distance[switchOff][2]=="b") {
      countB++;
    }
    stroke(180);
    if (j==k-1) {
      if (countA>countB) {
        fill('rgba(255,0,0, 0.05)');
        circle(mouseX, mouseY, 2*scale*min);
        fill('rgba(255,0,0, 1)');
        circle(mouseX, mouseY, 12);
        currClass = "a";
      } else {
        fill('rgba(0,0,255, 0.05)');
        circle(mouseX, mouseY, 2*scale*min);
        fill('rgba(0,0,255, 1)');
        circle(mouseX, mouseY, 12);
        currClass = "b";
      }
    }
    stroke(180);
    line(mouseX, mouseY, calcScreenX(Data[minIndex][0]), calcScreenY(Data[minIndex][1]));
    lowestValues.push([minIndex, Math.round(100*distance[minIndex][0])/100]);
    distance[switchOff][0]=1000;
  }
  makeTextarea();
}
function makeCoord(Width, margin, padding, scale) {
  noStroke();
  textSize(14);
  fill(128, 0, 0);
  textStyle(BOLD);
  text("number of neighbours k: " + k, Width + 100, 110);
  fill(255);
  noStroke();
  rect(margin-padding, margin-padding, Width+2*padding, Width+2*padding);
  fill(180);
  textSize(12);
  for (i=0; i<1+Width/scale; i++) {
    stroke(200);
    line(margin, margin+i*scale, Width+2*padding, margin+i*scale);
    line(margin+i*scale, margin, margin+i*scale, Width+2*padding);
    noStroke();
    if (i%2==0) {
      text(i.toString(), margin-6+i*scale, Width+12+2*padding);
      text(i.toString(), margin-16, Width+margin-i*scale);
    }
  }
  fill(160);
}
